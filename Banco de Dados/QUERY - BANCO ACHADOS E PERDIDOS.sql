							VESTIMENTA.COR AS VESTIMENTA_COR,
							VESTIMENTA.DESCRICAO AS VESTIMENTA_DESCRICAO
							
									FROM OCORRENCIA
					
					LEFT JOIN ANEXO ON ANEXO.CODANEXO = OCORRENCIA.CODANEXO
					LEFT JOIN ANIMAL ON ANIMAL.CODANIMAL = OCORRENCIA.CODANIMAL
					LEFT JOIN ARMAZENAMENTO ON ARMAZENAMENTO.CODARMAZENAMENTO = OCORRENCIA.CODARMAZENAMENTO
					LEFT JOIN CARTAO ON CARTAO.CODCARTAO=OCORRENCIA.CODCARTAO
					LEFT JOIN DOCUMENTO ON DOCUMENTO.CODDOCUMENTO = OCORRENCIA.CODDOCUMENTO
					LEFT JOIN ELETRONICO ON ELETRONICO.CODELETRONICO = OCORRENCIA.CODELETRONICO
					LEFT JOIN JOIA ON JOIA.CODJOIA = OCORRENCIA.CODJOIA
					LEFT JOIN LOCALIZACAO ON LOCALIZACAO.CODLOCALIZACAO = OCORRENCIA.CODLOCALIZACAO
					LEFT JOIN OBJETO ON OBJETO.CODOBJETO = OCORRENCIA.CODOBJETO
					LEFT JOIN VESTIMENTA ON VESTIMENTA.CODVESTIMENTA = OCORRENCIA.CODVESTIMENTA
					
