							ARMAZENAMENTO.COR AS ARMAZENAMENTO_COR,
							ARMAZENAMENTO.MARCA AS ARMAZENAMENTO_MARCA,
							ARMAZENAMENTO.NSERIE AS ARMAZENAMENTO_NSERIE,				
						
							CARTAO.CODCARTAO AS CARTAO_CODCARTAO,
							CARTAO.TIPO AS CARTAO_TIPO,
							CARTAO.NOME AS CARTAO_NOME,
							CARTAO.AGENCIA AS CARTAO_AGENCIA,
							CARTAO.CONTA AS CARTAO_CONTA,
							CARTAO.NUMERO AS CARTAO_NUMERO,
						
							DOCUMENTO.CODDOCUMENTO AS DOCUMENTO_CODDOCUMENTO,
							DOCUMENTO.CPF AS DOCUMENTO_CPF, 
							DOCUMENTO.RG AS DOCUMENTO_RG,
							DOCUMENTO.TITULO AS DOCUMENTO_TITULO,			
						
							ELETRONICO.CODELETRONICO AS ELETRONICO_CODELETRONICO,
