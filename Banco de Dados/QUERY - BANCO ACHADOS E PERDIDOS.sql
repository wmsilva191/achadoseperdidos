							ANEXO.CAMINHO AS ANEXO_CAMINHO,
							ANEXO.TIPO AS ANEXO_TIPO,
							ANEXO.TAMANHO AS ANEXO_TAMANHO,
							
							ANIMAL.CODANIMAL AS ANIMAL_CODANIMAL,
							ANIMAL.RACA AS ANIMAL_RACA, 
							ANIMAL.PORTE AS ANIMAL_PORTE,
							ANIMAL.COLEIRA AS ANIMAL_COLEIRA,
							ANIMAL.COLEIRA_COR AS ANIMAL_COLEIRA_COR,
							ANIMAL.COLEIRA_IDENTIFICACAO AS ANIMAL_COLEIRA_IDENTIFICACAO,
							ANIMAL.DEFICIENCIA AS ANIMAL_DEFICIENCIA,
							ANIMAL.CARACTERISTICA AS ANIMAL_CARACTERISTICA,
						
						
							ARMAZENAMENTO.CODARMAZENAMENTO AS ARMAZENAMENTO_CODARMAZENAMENTO, 
